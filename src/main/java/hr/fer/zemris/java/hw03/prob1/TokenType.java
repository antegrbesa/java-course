package hr.fer.zemris.java.hw03.prob1;

/**
 * Represents all the types a {@link Token} can be.
 * @author Ante Grbesa
 *
 */
public enum TokenType {
	/**
	 * End of file token 
	 */
	EOF,
	
	/**
	 * Word token
	 */
	WORD,
	
	/**
	 * Number token
	 */
	NUMBER,
	
	/**
	 * Symbol token
	 */
	SYMBOL;

}
